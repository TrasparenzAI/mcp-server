package it.cnr.anac.transparency.mcp_server.config;

import org.springframework.context.annotation.Bean;
import org.springframework.context.annotation.Configuration;
import org.springframework.security.config.annotation.web.builders.HttpSecurity;
import org.springframework.security.config.annotation.web.configurers.CsrfConfigurer;
import org.springframework.security.web.SecurityFilterChain;

/**
 * Permissive security configuration to avoid locking down endpoints when
 * spring-boot-starter-security is on the classpath. This does not configure
 * a Resource Server by default, so the application continues to accept
 * requests without authentication. If you configure JWT (issuer/jwk), the
 * SecurityContext will be populated and the Feign interceptor will pick up
 * the Bearer token from there.
 */
@Configuration
public class SecurityConfig {

    @Bean
    public SecurityFilterChain securityFilterChain(HttpSecurity http) throws Exception {
        http
            .csrf(CsrfConfigurer::disable)
            .authorizeHttpRequests(auth -> auth.anyRequest().permitAll());
        // Not enabling oauth2ResourceServer here to keep startup simple without issuer/jwk
        return http.build();
    }
}

package it.cnr.anac.transparency.mcp_server.dto;

import com.fasterxml.jackson.annotation.JsonIgnoreProperties;
import com.fasterxml.jackson.annotation.JsonInclude;

@JsonInclude(JsonInclude.Include.NON_NULL)
@JsonIgnoreProperties(ignoreUnknown = true)
public record CrawlerRequest(
        boolean verbose,
        CrawlingMode crawlingMode,
        boolean saveObject,
        boolean saveScreenshot,
        String url
) {
    public static CrawlerRequest of(CrawlingMode mode, String url) {
        return new CrawlerRequest(false, mode, false, false, url);
    }
}